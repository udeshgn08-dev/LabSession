/************************************
* Block letter pattern 
* Compilation: javac BlockLetter.java
* Execution: java BlockLetter
*************************************/
public class BlockLetter { 
    
  public static void main(String[] args) { 
  
    System.out.println("  *****  " + " *     * " + " *******"); 
    System.out.println(" *       " + " *     * " + " *        *"); 
    System.out.println("******** " + " *     * " + " *         *"); 
    System.out.println("*      * " + " *     * " + " *         *"); 
    System.out.println(" ******* " + " *     * " + " *         *"); 
    System.out.println("       * " + " *     * " + " *        *"); 
    System.out.println("       * " + "   * *  " + "  *******");
    
    }
}
