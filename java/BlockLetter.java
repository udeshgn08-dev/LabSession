package ch01.basics;

public class BlockLetter {	    
	  public static void main(String[] args) { 
	  
	    System.out.println("  *****  " + " *     * " + " *******"); 
	    System.out.println(" *       " + " *     * " + " *        *"); 
	    System.out.println("******** " + " *     * " + " *         *"); 
	    System.out.println("*      * " + " *     * " + " *         *"); 
	    System.out.println(" ******* " + " *     * " + " *         *"); 
	    System.out.println("       * " + " *     * " + " *        *"); 
	    System.out.println("       * " + "   * *  " + "  *******");
	    
	    }
	}