/**
 * Java is a statically typed language meaning all variables must be type declared before using it. 
 * Numeric Primitive types are int, long, double, float, byte, short
 */
package practise;
public class P3Helper {

	public static void main(String[] args) {
		
          byte byt = 127; 
          short sht = 32000; 
          int in_t = 10000; 
          long lng = 1000000L; 
          float flt = 5689.5F; 
          double dbl = 9054.1D; 
          System.out.println("Byte: "+byt);
          System.out.println("Short: "+sht);
          System.out.println("Integer: "+in_t);
          System.out.println("Long: "+lng);
          System.out.println("Float: "+flt);
          System.out.println("Double: "+dbl);
	}

}
