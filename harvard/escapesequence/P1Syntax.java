package set00;

public class P1Syntax {

	public static void main(String[] args) { 
		// General syntax 
		int firstNumber = 5;
		int secondNumber = 5;
		int product = firstNumber * secondNumber; 
		System.out.println("\tProduct of 5*5=" + product);
		System.out.println("\tProduct of 5*5=" + (firstNumber * secondNumber));
		System.out.println("\tProduct of 5*5=" + 5 * 5);

	}
}
